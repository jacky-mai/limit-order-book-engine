"""## 4. Microstructure Metrics

- **Bid-ask spread & order book imbalance** — already computed per snapshot in Section 3.
- **Kyle's (1985) lambda** — price impact per unit of signed order flow, estimated via rolling OLS.
- **Realized volatility vs. Two-Scale Realized Volatility (TSRV)** — the naive "all-tick"
  realized variance is biased upward by bid-ask bounce; Zhang, Mykland & Aït-Sahalia (2005)
  correct this by combining a sparse-sampled ("slow") estimator with the full-tick ("fast")
  estimator.

"""

# ---------------------------------------------------------------------------
# 4. MICROSTRUCTURE METRICS
# ---------------------------------------------------------------------------
def make_regular_price_grid(trades, duration, freq=0.25, price_col='price'):
    """Resample trade prices onto a regular time grid via last-observed-price
    forward fill ('previous tick' scheme) -- the standard approach for
    realized-variance estimation under irregular tick-time sampling."""
    if trades.empty:
        raise ValueError("No trades available to build a price grid.")
    grid = np.arange(0, duration, freq)
    idx = np.searchsorted(trades['timestamp'].values, grid, side='right') - 1
    idx = np.clip(idx, 0, len(trades) - 1)
    return pd.Series(trades[price_col].values[idx], index=grid)


def realized_variance(log_returns):
    return np.sum(log_returns ** 2)


def two_scale_realized_variance(price_series, k_sparse=5):
    """Zhang, Mykland & Ait-Sahalia (2005) two-scale realized volatility (TSRV)."""
    log_p = np.log(price_series.values)
    n = len(log_p) - 1
    if n < k_sparse * 2:
        raise ValueError("Series too short for the requested number of subgrids.")

    all_returns = np.diff(log_p)
    rv_all = realized_variance(all_returns)

    subgrid_rvs, subgrid_sizes = [], []
    for start in range(k_sparse):
        sub = log_p[start::k_sparse]
        if len(sub) < 2:
            continue
        subgrid_rvs.append(realized_variance(np.diff(sub)))
        subgrid_sizes.append(len(sub) - 1)
    rv_avg = np.mean(subgrid_rvs)
    n_bar = np.mean(subgrid_sizes)

    tsrv = rv_avg - (n_bar / n) * rv_all
    return {'rv_all_ticks': rv_all, 'rv_sparse_avg': rv_avg,
            'tsrv': max(tsrv, 0.0), 'n_bar': n_bar, 'n': n}


def rolling_kyle_lambda(trades, snapshots, bucket_size=5.0, window_buckets=20):
    """Kyle's (1985) lambda: regress bucketed mid-price changes on bucketed
    net signed trade volume, delta_mid = lambda * signed_volume + epsilon,
    estimated via rolling OLS."""
    tr = trades.copy()
    tr['signed_size'] = np.where(tr['aggressor_side'] == 'BUY', tr['size'], -tr['size'])
    tr['bucket'] = (tr['timestamp'] // bucket_size).astype(int)
    signed_vol = tr.groupby('bucket')['signed_size'].sum()

    sn = snapshots.copy()
    sn['bucket'] = (sn['timestamp'] // bucket_size).astype(int)
    mid_by_bucket = sn.groupby('bucket')['mid_price'].last()

    df = pd.concat([mid_by_bucket, signed_vol], axis=1).sort_index()
    df.columns = ['mid_price', 'signed_volume']
    df['mid_price'] = df['mid_price'].ffill()
    df['signed_volume'] = df['signed_volume'].fillna(0.0)
    df['delta_mid'] = df['mid_price'].diff()
    df = df.dropna()

    lambdas, r2s, centers = [], [], []
    for i in range(window_buckets, len(df)):
        window = df.iloc[i - window_buckets:i]
        if window['signed_volume'].std() == 0:
            continue
        slope, intercept, r, p, se = stats.linregress(window['signed_volume'], window['delta_mid'])
        lambdas.append(slope)
        r2s.append(r ** 2)
        centers.append(window.index[-1])

    return pd.DataFrame({'bucket': centers, 'kyle_lambda': lambdas, 'r_squared': r2s})


