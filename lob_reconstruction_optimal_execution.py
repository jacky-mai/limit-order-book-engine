"""## 7. Avellaneda-Stoikov Market Making

The Avellaneda-Stoikov (2008) model derives the inventory-aware reservation
price and optimal bid/ask spread for a market maker who wants to earn the
spread while controlling inventory risk, under an assumed exponential
order-arrival intensity `λ(δ) = A·exp(-k·δ)`. We simulate this against a naive
fixed-spread, no-inventory-skew benchmark.

"""

# ---------------------------------------------------------------------------
# 7. AVELLANEDA-STOIKOV MARKET MAKING
# ---------------------------------------------------------------------------
def simulate_avellaneda_stoikov(S0, T, n_steps, sigma, gamma, k, A, seed=0):
    """Avellaneda & Stoikov (2008) inventory-aware market-making simulation."""
    if sigma < 0 or gamma <= 0 or k <= 0 or A <= 0 or n_steps < 1 or T <= 0:
        raise ValueError("Invalid parameter: require sigma>=0, gamma,k,A>0, n_steps>=1, T>0.")

    rng = np.random.default_rng(seed)
    dt = T / n_steps
    s, q, cash = S0, 0, 0.0
    rows = []
    for i in range(n_steps):
        t = i * dt
        time_left = T - t
        r = s - q * gamma * sigma ** 2 * time_left
        spread = gamma * sigma ** 2 * time_left + (2.0 / gamma) * np.log(1.0 + gamma / k)
        bid, ask = r - spread / 2.0, r + spread / 2.0

        delta_b, delta_a = max(s - bid, 0.0), max(ask - s, 0.0)
        p_fill_bid = 1.0 - np.exp(-A * np.exp(-k * delta_b) * dt)
        p_fill_ask = 1.0 - np.exp(-A * np.exp(-k * delta_a) * dt)
        filled_bid = rng.random() < p_fill_bid
        filled_ask = rng.random() < p_fill_ask

        if filled_bid:
            q += 1; cash -= bid
        if filled_ask:
            q -= 1; cash += ask

        rows.append({'time': t, 'mid_price': s, 'reservation_price': r, 'bid': bid, 'ask': ask,
                     'spread': spread, 'inventory': q, 'cash': cash, 'pnl': cash + q * s})
        s = s + sigma * np.sqrt(dt) * rng.standard_normal()
    return pd.DataFrame(rows)


def simulate_naive_market_maker(S0, T, n_steps, sigma, fixed_half_spread, k, A, seed=0):
    """Benchmark: symmetric fixed-spread quoting with NO inventory skew."""
    rng = np.random.default_rng(seed)
    dt = T / n_steps
    s, q, cash = S0, 0, 0.0
    rows = []
    p_fill = 1.0 - np.exp(-A * np.exp(-k * fixed_half_spread) * dt)
    for i in range(n_steps):
        t = i * dt
        bid, ask = s - fixed_half_spread, s + fixed_half_spread
        filled_bid = rng.random() < p_fill
        filled_ask = rng.random() < p_fill
        if filled_bid:
            q += 1; cash -= bid
        if filled_ask:
            q -= 1; cash += ask
        rows.append({'time': t, 'mid_price': s, 'inventory': q, 'cash': cash, 'pnl': cash + q * s})
        s = s + sigma * np.sqrt(dt) * rng.standard_normal()
    return pd.DataFrame(rows)


