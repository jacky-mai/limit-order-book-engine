def load_lobster_events(message_path, price_scale=10000, side_convention=None):
    """
    Parse a LOBSTER message file into the internal events schema:
    [timestamp, event_type, side, price, size, order_id].

    LOBSTER message file columns (documented format):
        Time, Type, OrderID, Size, Price, Direction
        Type: 1=new limit order, 2=partial cancel, 3=full cancel/delete,
              4=visible execution, 5=hidden execution, 7=trading halt
        Direction: 1=buy limit order resting, -1=sell limit order resting
                   (for executions, this is the resting order's side --
                   the AGGRESSOR side is the opposite sign)

    Parameters
    ----------
    message_path : str, path to the LOBSTER '*_message_*.csv' file
    price_scale   : divisor to convert integer prices to dollars (default 10000)
    side_convention : optional dict to override the default {1: 'BUY', -1: 'SELL'}
                       mapping if your file uses a different convention.

    Returns
    -------
    pandas.DataFrame with columns matching LOBSimulator.run()'s events_df,
    or raises FileNotFoundError with a clear message if the file is missing.
    """
    side_convention = side_convention or {1: 'BUY', -1: 'SELL'}
    try:
        raw = pd.read_csv(message_path, header=None,
                           names=['time', 'type', 'order_id', 'size', 'price', 'direction'])
    except FileNotFoundError:
        raise FileNotFoundError(
            f"LOBSTER message file not found at '{message_path}'. "
            "Download a sample from https://lobsterdata.com and update the path, "
            "or continue using the synthetic simulator in Section 2A."
        )

    raw['price'] = raw['price'] / price_scale
    type_map = {1: 'LIMIT_ADD', 2: 'CANCEL', 3: 'CANCEL', 4: 'MARKET_ORDER', 5: 'MARKET_ORDER'}
    raw = raw[raw['type'].isin(type_map.keys())].copy()
    raw['event_type'] = raw['type'].map(type_map)
    raw['side'] = raw['direction'].map(side_convention)
    # For executions, the resting order's direction is opposite the aggressor;
    # flip side so 'side' consistently reflects the AGGRESSOR for MARKET_ORDER rows.
    is_exec = raw['event_type'] == 'MARKET_ORDER'
    raw.loc[is_exec, 'side'] = raw.loc[is_exec, 'side'].map({'BUY': 'SELL', 'SELL': 'BUY'})

    events = raw.rename(columns={'time': 'timestamp'})[
        ['timestamp', 'event_type', 'side', 'price', 'size', 'order_id']
    ].reset_index(drop=True)
    return events


