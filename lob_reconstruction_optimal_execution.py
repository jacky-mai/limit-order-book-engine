"""## 2A. Synthetic Order-Flow Generator

New limit orders are placed relative to the **live, current** mid-price of
the book (not a fixed anchor). This is what makes price evolve endogenously
from order-flow imbalance, rather than mean-reverting artificially to an
arbitrary constant.

"""

# ---------------------------------------------------------------------------
# 2A. SYNTHETIC ORDER-FLOW GENERATOR (data source #1 -- always available)
# ---------------------------------------------------------------------------
class LimitOrderBook:
    """Price-time priority limit order book (single-instrument matching engine)."""

    def __init__(self):
        self.bids = {}          # price -> deque([order_id, size], ...)
        self.asks = {}          # price -> deque([order_id, size], ...)
        self.order_index = {}   # order_id -> (side, price)
        self.trade_log = []     # (timestamp, price, size, aggressor_side)
        self.unfilled_market_shares = 0   # diagnostic: liquidity shortfall counter
        self.unfilled_market_events = 0

    def add_limit_order(self, order_id, side, price, size, timestamp):
        """
        Add a limit order, first checking whether it CROSSES the book (a
        'marketable limit order'). Real exchanges match crossing limit
        orders immediately rather than resting them -- skipping this check
        is a common implementation bug that produces an impossible crossed
        book (best_bid > best_ask).
        """
        remaining = size
        if side == 'BUY':
            while remaining > 0 and self.asks and min(self.asks.keys()) <= price:
                best_ask = min(self.asks.keys())
                dq = self.asks[best_ask]
                while dq and remaining > 0:
                    oid, sz = dq[0]
                    fill_qty = min(sz, remaining)
                    self.trade_log.append((timestamp, best_ask, fill_qty, 'BUY'))
                    remaining -= fill_qty
                    if fill_qty == sz:
                        dq.popleft()
                        self.order_index.pop(oid, None)
                    else:
                        dq[0][1] -= fill_qty
                if not dq:
                    del self.asks[best_ask]
            if remaining > 0:
                self.bids.setdefault(price, deque()).append([order_id, remaining])
                self.order_index[order_id] = ('BUY', price)
        else:
            while remaining > 0 and self.bids and max(self.bids.keys()) >= price:
                best_bid = max(self.bids.keys())
                dq = self.bids[best_bid]
                while dq and remaining > 0:
                    oid, sz = dq[0]
                    fill_qty = min(sz, remaining)
                    self.trade_log.append((timestamp, best_bid, fill_qty, 'SELL'))
                    remaining -= fill_qty
                    if fill_qty == sz:
                        dq.popleft()
                        self.order_index.pop(oid, None)
                    else:
                        dq[0][1] -= fill_qty
                if not dq:
                    del self.bids[best_bid]
            if remaining > 0:
                self.asks.setdefault(price, deque()).append([order_id, remaining])
                self.order_index[order_id] = ('SELL', price)

    def cancel_order(self, order_id):
        info = self.order_index.pop(order_id, None)
        if info is None:
            return False  # already filled or cancelled -- not an error
        side, price = info
        book = self.bids if side == 'BUY' else self.asks
        dq = book.get(price)
        if not dq:
            return False
        for i, (oid, _sz) in enumerate(dq):
            if oid == order_id:
                del dq[i]
                break
        if not dq:
            del book[price]
        return True

    def execute_market_order(self, side, size, timestamp):
        book = self.asks if side == 'BUY' else self.bids
        remaining = size
        fills = []
        levels = sorted(book.keys()) if side == 'BUY' else sorted(book.keys(), reverse=True)
        for price in levels:
            if remaining <= 0:
                break
            dq = book[price]
            while dq and remaining > 0:
                oid, sz = dq[0]
                fill_qty = min(sz, remaining)
                fills.append((timestamp, price, fill_qty, side))
                remaining -= fill_qty
                if fill_qty == sz:
                    dq.popleft()
                    self.order_index.pop(oid, None)
                else:
                    dq[0][1] -= fill_qty
            if not dq:
                del book[price]
        self.trade_log.extend(fills)
        if remaining > 0:
            # Market order larger than total resting liquidity on this side.
            # In a real venue this would trigger a halt / reject; here we
            # track it as an aggregate diagnostic rather than warning on
            # every occurrence (which would flood output at realistic
            # order-flow rates).
            self.unfilled_market_shares += remaining
            self.unfilled_market_events += 1
        return fills, remaining

    def best_bid(self):
        return max(self.bids) if self.bids else np.nan

    def best_ask(self):
        return min(self.asks) if self.asks else np.nan

    def mid_price(self):
        bb, ba = self.best_bid(), self.best_ask()
        return np.nan if (np.isnan(bb) or np.isnan(ba)) else 0.5 * (bb + ba)

    def spread(self):
        bb, ba = self.best_bid(), self.best_ask()
        return np.nan if (np.isnan(bb) or np.isnan(ba)) else ba - bb

    def depth_snapshot(self, num_levels=5):
        bid_levels = sorted(self.bids.keys(), reverse=True)[:num_levels]
        ask_levels = sorted(self.asks.keys())[:num_levels]
        bid_sizes = [sum(sz for _, sz in self.bids[p]) for p in bid_levels]
        ask_sizes = [sum(sz for _, sz in self.asks[p]) for p in ask_levels]
        return bid_levels, bid_sizes, ask_levels, ask_sizes


class LOBSimulator:
    """
    Integrated synthetic order-flow generator + matching engine. Order
    arrivals follow independent Poisson processes (limit adds, market
    orders, cancellations); new limit orders are placed relative to the
    CURRENT live mid-price so price evolves endogenously from order-flow
    imbalance rather than mean-reverting to a fixed anchor.
    """

    def __init__(self, initial_price=100.00, tick_size=0.01, num_levels=8,
                 limit_rate=3.0, market_rate=1.6, cancel_rate=2.2,
                 mean_limit_size=40, mean_market_size=35,
                 orders_per_level=2, bootstrap_size=30, seed=42):
        if min(limit_rate, market_rate, cancel_rate) < 0:
            raise ValueError("Arrival rates must be non-negative.")
        if tick_size <= 0 or num_levels < 1:
            raise ValueError("tick_size must be positive and num_levels >= 1.")

        self.initial_price = initial_price
        self.tick_size = tick_size
        self.num_levels = num_levels
        self.limit_rate = limit_rate
        self.market_rate = market_rate
        self.cancel_rate = cancel_rate
        self.mean_limit_size = mean_limit_size
        self.mean_market_size = mean_market_size
        self.orders_per_level = orders_per_level
        self.bootstrap_size = bootstrap_size
        self.rng = np.random.default_rng(seed)
        self._order_counter = 0
        self.lob = LimitOrderBook()
        self._bootstrap()

    def _new_id(self):
        self._order_counter += 1
        return self._order_counter

    def _bootstrap(self):
        """Seed the book with initial resting liquidity so it is never empty."""
        for lvl in range(self.num_levels):
            offset = (lvl + 1) * self.tick_size
            bid_px = round(self.initial_price - offset, 2)
            ask_px = round(self.initial_price + offset, 2)
            for _ in range(self.orders_per_level):
                size = max(1, int(self.rng.lognormal(np.log(self.bootstrap_size), 0.4)))
                self.lob.add_limit_order(self._new_id(), 'BUY', bid_px, size, 0.0)
                size = max(1, int(self.rng.lognormal(np.log(self.bootstrap_size), 0.4)))
                self.lob.add_limit_order(self._new_id(), 'SELL', ask_px, size, 0.0)

    def _reference_price(self):
        m = self.lob.mid_price()
        return m if not np.isnan(m) else self.initial_price

    def run(self, duration=500.0, num_levels_snapshot=5, snapshot_every=1):
        """Run the simulation and return (snapshots_df, trades_df, events_df)."""
        total_rate = self.limit_rate + self.market_rate + self.cancel_rate
        if total_rate <= 0:
            raise ValueError("Total event rate must be positive.")

        t = 0.0
        snapshots, event_log = [], []
        n_events = 0

        while t < duration:
            dt = self.rng.exponential(1.0 / total_rate)
            t += dt
            if t >= duration:
                break
            n_events += 1
            u = self.rng.random() * total_rate
            ref = self._reference_price()

            if u < self.limit_rate:
                side = 'BUY' if self.rng.random() < 0.5 else 'SELL'
                level = min(self.rng.geometric(p=0.4) - 1, self.num_levels - 1)
                offset = (level + 1) * self.tick_size
                price = round(ref - offset, 2) if side == 'BUY' else round(ref + offset, 2)
                size = max(1, int(self.rng.lognormal(np.log(self.mean_limit_size), 0.5)))
                oid = self._new_id()
                self.lob.add_limit_order(oid, side, price, size, t)
                event_log.append((t, 'LIMIT_ADD', side, price, size, oid))

            elif u < self.limit_rate + self.market_rate:
                side = 'BUY' if self.rng.random() < 0.5 else 'SELL'
                size = max(1, int(self.rng.lognormal(np.log(self.mean_market_size), 0.6)))
                self.lob.execute_market_order(side, size, t)
                event_log.append((t, 'MARKET_ORDER', side, None, size, None))

            else:
                if self.lob.order_index:
                    oid = self.rng.choice(list(self.lob.order_index.keys()))
                    side = self.lob.order_index[oid][0]
                    self.lob.cancel_order(oid)
                    event_log.append((t, 'CANCEL', side, None, None, oid))

            if n_events % snapshot_every == 0:
                bb, ba = self.lob.best_bid(), self.lob.best_ask()
                if np.isnan(bb) or np.isnan(ba):
                    continue
                if bb > ba:
                    # Defensive invariant check -- should be structurally
                    # impossible given the crossing-order logic above.
                    warnings.warn(f"Crossed book detected at t={t:.4f}: bid={bb} > ask={ba}")
                bid_lv, bid_sz, ask_lv, ask_sz = self.lob.depth_snapshot(num_levels_snapshot)
                bid_sz_p = bid_sz + [0] * (num_levels_snapshot - len(bid_sz))
                ask_sz_p = ask_sz + [0] * (num_levels_snapshot - len(ask_sz))
                row = {
                    'timestamp': t, 'best_bid': bb, 'best_ask': ba,
                    'mid_price': 0.5 * (bb + ba), 'spread': ba - bb,
                    'bid_depth_total': sum(bid_sz), 'ask_depth_total': sum(ask_sz),
                    'imbalance': (sum(bid_sz) - sum(ask_sz)) / (sum(bid_sz) + sum(ask_sz) + 1e-9),
                }
                for i in range(num_levels_snapshot):
                    row[f'bid_sz_L{i+1}'] = bid_sz_p[i]
                    row[f'ask_sz_L{i+1}'] = ask_sz_p[i]
                snapshots.append(row)

        snap_df = pd.DataFrame(snapshots)
        trades_df = pd.DataFrame(self.lob.trade_log,
                                  columns=['timestamp', 'price', 'size', 'aggressor_side'])
        events_df = pd.DataFrame(event_log,
                                  columns=['timestamp', 'event_type', 'side', 'price', 'size', 'order_id'])
        return snap_df, trades_df, events_df


