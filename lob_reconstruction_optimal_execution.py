"""## 6. Almgren-Chriss Optimal Execution

Given a total order size to liquidate over a fixed horizon, the Almgren-Chriss
(2000/2001) framework derives the trading trajectory that minimizes a
mean-variance objective over execution cost: trade too fast and permanent +
temporary market impact erode proceeds; trade too slow and price-path
uncertainty (volatility) increases cost variance. The risk-aversion
parameter **λ** controls where you sit on this trade-off.

"""

# ---------------------------------------------------------------------------
# 6. ALMGREN-CHRISS OPTIMAL EXECUTION
# ---------------------------------------------------------------------------
def almgren_chriss_trajectory(X, T, N, sigma, eta, gamma, lam):
    """
    Almgren & Chriss (2000/2001) optimal execution trajectory.

    Returns (t, x, n): time grid, shares REMAINING at each time point
    (x[0]=X, x[-1]=0), and shares TRADED in each interval.
    lam -> 0 recovers exact TWAP (uniform liquidation).
    """
    if X <= 0 or T <= 0 or N < 1:
        raise ValueError("X, T must be positive and N must be a positive integer.")
    if sigma < 0 or eta <= 0 or gamma < 0 or lam < 0:
        raise ValueError("sigma, gamma, lam must be >= 0 and eta must be > 0.")

    tau = T / N
    eta_tilde = eta - gamma * tau / 2.0
    if eta_tilde <= 0:
        raise ValueError(
            "eta - gamma*tau/2 must be positive; reduce gamma, increase eta, "
            "or use more/shorter trading intervals (smaller tau)."
        )

    t = np.linspace(0, T, N + 1)
    if lam <= 1e-12:
        x = X * (1.0 - t / T)  # risk-neutral limit == TWAP
    else:
        kappa_tilde_sq = lam * sigma ** 2 / eta_tilde
        kappa = np.arccosh(tau ** 2 * kappa_tilde_sq / 2.0 + 1.0) / tau
        x = X * np.sinh(kappa * (T - t)) / np.sinh(kappa * T)

    n = -np.diff(x)
    return t, x, n


def simulate_execution_cost(n_trades, tau, sigma, eta, gamma, arrival_price,
                             n_paths=3000, seed=0):
    """
    Monte Carlo implementation-shortfall simulation under Almgren-Chriss
    price dynamics:
        S_k = S_(k-1) + sigma*sqrt(tau)*Z_k - gamma*n_k          (permanent impact)
        exec_price_k = S_(k-1) - gamma*n_k/2 - eta*(n_k/tau)     (temporary impact)
    Cost = shortfall in dollars relative to arrival price (SELL program).
    """
    rng = np.random.default_rng(seed)
    N = len(n_trades)
    Z = rng.standard_normal((n_paths, N))
    S = np.full(n_paths, arrival_price, dtype=float)
    total_cost = np.zeros(n_paths)
    for k in range(N):
        nk = n_trades[k]
        exec_price = S - gamma * nk / 2.0 - eta * (nk / tau)
        total_cost += nk * (arrival_price - exec_price)
        S = S + sigma * np.sqrt(tau) * Z[:, k] - gamma * nk
    return total_cost


def efficient_frontier(X, T, N, sigma, eta, gamma, arrival_price,
                        lam_grid=None, n_paths=3000, seed=0):
    """Trace expected cost vs. cost variance across a grid of risk-aversion levels."""
    if lam_grid is None:
        lam_grid = np.concatenate(([0.0], np.logspace(-8, -3, 15)))
    rows = []
    for lam in lam_grid:
        t, x, n = almgren_chriss_trajectory(X, T, N, sigma, eta, gamma, lam)
        costs = simulate_execution_cost(n, T / N, sigma, eta, gamma, arrival_price,
                                         n_paths=n_paths, seed=seed)
        rows.append({'lambda': lam, 'expected_cost': costs.mean(),
                     'cost_std': costs.std(), 'cost_variance': costs.var()})
    return pd.DataFrame(rows)


